<blockquote>
Recuerde incluir como parte de su hogar a los siguientes <strong>RESIDENTES HABITUALES</strong>, porque no tienen residencia habitual en otro hogar:
</blockquote>
<ul>
<li>1) Personas que están ausentes por 6 meses o menos por motivos especiales como: vacaciones, cursos de capacitación, viajes de negocio, comisiones de trabajo, entre otros; y además, cuentan con la seguridad de que van a regresar al hogar después de estos 6 meses o menos.</li>
<li>2) Agentes viajeros, marinos mercantes.</li>
<li>3) Secuestrados y desaparecidos, sin importar el tiempo de ausencia.</li>
<li>4) Enfermos internados en hospitales o clínicas, sin importar el tiempo de ausencia.</li>
<li>5) Desplazados, sin importar el tiempo de permanencia en el hogar encuestado.</li>
<li>6) Detenidos temporalmente en inspecciones de policía.</li>
<li>7) Empleados internos del servicio doméstico</li>
<li>8) Personas que están prestando el servicio militar en la policía pero duermen en sus respectivos hogares.</li>
<li>9) Pensionistas </li>
<li>10) Residentes de casas fiscales al interior de guarniciones militares.</li>
</ul>
<br/>
<strong>NO</strong> incluya como <strong>RESIDENTES HABITUALES</strong>:
<ul>
<li>1) Personas que aunque sean consideradas como el principal soporte económico del hogar y estén presentes en el hogar cada fin de semana o cada 15 días, lleven 6 meses o más ausentes y tengan residencia en otro hogar porque permanecen la mayor parte del tiempo en un sitio diferente.</li>
<li>2) Personas que en el momento de la encuesta están pagando condenas en cárceles, prestando servicio militar en cuarteles del ejército, la fuerza aérea o en la amada nacional; están internos en instituciones educativas, asilos, conventos o monasterios.</li>
</ul>
